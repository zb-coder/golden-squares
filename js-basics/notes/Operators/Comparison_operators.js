
let x = 1;

// The result of an expression which includes a comparison operator (e.g <, >, ===) is a boolean
// Relational Operators
console.log( x < 0);
console.log( x > 0);
console.log( x <= 0);
console.log( x >= 0);

// Equality Operators
console.log(x === 1); // checks if x is equal to 1
console.log(x !== 1); // checks if x is not equal to 1

